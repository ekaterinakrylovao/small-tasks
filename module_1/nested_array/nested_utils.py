"""
Модуль содержит функцию для проверки возможности вложения одного массива в другой.
"""

from typing import List


def is_nestable(a1: List[int], a2: List[int]) -> bool:
    """
    Проверяет, можно ли вложить массив a1 в массив a2.

    Условия вложенности:
    - Минимальный элемент a1 должен быть больше минимального элемента a2.
    - Максимальный элемент a1 должен быть меньше максимального элемента a2.

    :param a1: Первый массив (кандидат на вложение)
    :param a2: Второй массив (контейнер)
    :return: True, если a1 можно вложить в a2, иначе False.
    """
    if not a1 or not a2:
        raise ValueError("Оба массива должны содержать хотя бы один элемент")

    return min(a1) > min(a2) and max(a1) < max(a2)
