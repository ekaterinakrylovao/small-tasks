"""
Модуль содержит функцию для исправления перепутанных строк.
"""


def fix_string(s: str) -> str:
    """
    Исправляет строку, в которой каждая пара символов поменялась местами.

    Пример:
    - fix_string("badce") -> "abcde"

    Если длина строки нечётная, последний символ остаётся на месте.

    :param s: Исходная перепутанная строка.
    :return: Исправленная строка.
    """
    if not isinstance(s, str):
        raise TypeError("Входные данные должны быть строкой")

    return "".join(s[i + 1] + s[i] if i + 1 < len(s) else s[i] for i in range(0, len(s), 2))
